// Render function
function renderMenuItems(items) {
    const container = document.getElementById("menuItemsContainer");

    if (items.length === 0) {
        container.innerHTML = `<p class="text-center mt-4 fw-bold fs-4">No dishes found</p>`;
        container.style.display = "block"; // show container to display message
        return;
    }

    container.innerHTML = ""; // clear previous content
    items.forEach(item => {
        const html = `
        <div class="row justify-content-center mb-4">
            <div class="col-12 col-md-11 col-lg-9 col-xl-7">
                <div class="blk-card rounded-4 px-3 py-4 px-md-4 py-md-4">
                    <div class="d-flex flex-column flex-md-row align-items-center gap-4">

                        <div class="flex-shrink-0">
                            <img src="${item.image}" class="img-fluid rounded-circle shadow" style="width: 160px; height: 160px; object-fit: cover;" alt="">
                        </div>

                        <div class="flex-grow-1 d-flex flex-column flex-md-row justify-content-between align-items-md-center w-100">
                            <div class="text-center text-md-start ms-3">
                                <div class="blk-cat mb-1 fs-6 fs-md-6">${item.category}</div>
                                <h3 class="blk-title fw-bold text-uppercase mb-1 mb-md-2 fs-6 fs-md-3">${item.title}</h3>
                                <p class="price-value fw-semibold mb-0 fs-6 fs-md-5 mt-1 mt-md-3">
                                    ${item.label}
                                    <span class="ms-2">${item.price}</span>
                                </p>
                            </div>

                            <div class="mt-3 mt-md-0 text-center">
                                <a href="contact.html" class="blk-add btn btn-success rounded-pill px-4 py-2 fs-6 fs-md-6">Contact</a>
                            </div>
                        </div>

                    </div>
                </div>
            </div>
        </div>`;
        container.insertAdjacentHTML("beforeend", html);
    });

    container.style.display = "block"; // show container
}

// Initial load — hide container
document.addEventListener("DOMContentLoaded", function () {
    const container = document.getElementById("menuItemsContainer");
    container.style.display = "none"; // hide initially
});

// Live search
document.getElementById("navSearchInput").addEventListener("input", function () {
    const text = this.value.toLowerCase().trim();
    const container = document.getElementById("menuItemsContainer");

    if (text === "") {
        container.style.display = "none"; // hide container when search is empty
        container.innerHTML = "";
        return;
    }

    const filtered = dishes.filter(item =>
        item.title.toLowerCase().includes(text) ||
        item.category.toLowerCase().includes(text)
    );

    renderMenuItems(filtered);
});